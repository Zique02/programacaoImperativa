let produtosCompra = ['ps5', 'xbox one', 'nintendo switch']

console.log('O método join une todos os elementos presentes no array em uma unica string')
console.log('RESULTADO_JOIN', '\n', produtosCompra.join(', '))
console.log(produtosCompra)

console.log('O método pop retira elementos')
console.log('RESULTADO_POP', '\n', produtosCompra.pop())
console.log(produtosCompra)

console.log('O método Push insere elementos no final do array')
console.log('RESULTADO_PUSH', '\n', produtosCompra.push('Dell Inspiron'))
console.log(produtosCompra)

console.log('O método Shift para retirar o primeiro elemento do array')
console.log('RESULTADO_SHIFT', '\n', produtosCompra.shift())
console.log(produtosCompra)

console.log('O método Unshift para inserir elementos no inicio de um array');
console.log('RESULTADO_UNSHIFT', '\n', produtosCompra.unshift('ps4'))


console.log(produtosCompra)